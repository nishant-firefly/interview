# Python is a high-level, interpreted programming language known for its simplicity and readability.
# It's widely used for web development, data analysis, artificial intelligence, scientific computing, and more.
# You can download Python from the official website (python.org) and follow the installation instructions for your operating system.
# Make sure to check the option to add Python to your system's PATH during installation for easier command-line access.
# Once Python is installed, you can open a command prompt or terminal and type python to enter the Python interpreter.
# The interpreter allows you to execute Python code interactively.
# Python uses indentation to define code blocks, such as loops and functions, instead of curly braces or keywords like begin and end.
#For example, a simple "Hello, World!" program looks like this:
print("Hello, World!")
# Variables are used to store data in Python. You don't need to declare the data type explicitly; Python infers it based on the value assigned.
# Common data types include integers, floats (decimal numbers), strings (text), booleans (True/False), lists, tuples, dictionaries, and sets.
# Python uses control structures like if statements, loops, and functions for decision-making and repetitive tasks.
# Example of an if statement:
x = 10
if x > 5:
    print("x is greater than 5")
# Functions in Python are blocks of reusable code that perform a specific task. They can accept inputs (parameters) and return outputs.
# Example of a function:
def greet(name):
    print(f"Hello, {name}!")

greet("Alice")
# Lists are used to store multiple items in a single variable. They can be modified (mutable).
# Loops like for loops and while loops are used to iterate over lists, strings, or perform repetitive tasks.
# Example of a for loop with a list:
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)
# Python provides functions like input() to take user input and print() to display output.
# Example of input and output:
name = input("Enter your name: ")
print(f"Hello, {name}!")
